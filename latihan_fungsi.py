'''Fungsi dengan parameter'''
'''def nama_fungsi(p1, p2)'''

def say_hi(nama):
  print("Halo perkenalkan nama saya", nama)

say_hi("Jawir")
say_hi("Reog")

'''buatlah fungsi dengan 3 parameter'''

def nama_orang(nama, hobi, umur):
  print("Nama gua:", nama)
  print("Hobi gua:", hobi)
  print("Umur gua:", umur)

nama_orang("Patan", "Tidur", 16)